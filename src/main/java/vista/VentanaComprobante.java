    public VentanaComprobante(Credito credito) {
        this.credito = credito;
        initComponents();
        String s;
        if (credito.getPlan().isEsadelantado()) {
            s = "(primera adelantada)";
            lblGastoAdmin.setVisible(false);
        } else {
            s = "(primera vencida)";
            lblGastoAdmin.setText("Importe de gastos: $" + credito.getGastosadmin());
        }
        lblCuota.setText("Importe de cuota: $" + String.format("%.2f", credito.getCuotas().get(0).getPrecio()));
        lblMonto.setText("Monto solicitado: $" + credito.getMontosolicitado());
        lblMontoEntregado.setText("Importe entregado al cliente: $" + credito.getMontoaentregar());
        lblPlan.setText("Plan: "
                + credito.getPlan().getCantcuotas()
                + " Cuotas "
                + s 
                + " - Porc. gastos: " 
                + (credito.getPlan().getGastosadmin()*100) 
                + "% - Int. Men. : "
                + (credito.getPlan().getInteres()*100)
                + "%"
            );
        lblTotal.setText("Monto total del cr√©dito: $ " + credito.getTotal());
    }

